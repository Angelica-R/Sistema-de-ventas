    private void MtdListar() {
        ResultSet rs;//iniciando nuestro contenedor de datos
        rs=objNEmp.MtdListar_Empleados();//rs contiene el resultad de la consulta al BD
        try {
            while (rs.next())//inicia desde el primer elemento hasta el final de la consulta DB
            {
                modelo.addRow(new Object[]{rs.getInt("dni"),rs.getString("nombres"),rs.getString("apellidos"),
                    rs.getString("cargo"),rs.getString("estado"),rs.getString("clave")});
            }
            Tabla.setModel(modelo);
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }
    
    private void MtdlimpiarTabla(){ 
        modelo.getDataVector().removeAllElements();
        Tabla.updateUI();
    }

    
