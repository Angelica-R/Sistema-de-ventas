    public void fecha_actual() { 
        Date sisFecha = new Date(); 
        SimpleDateFormat formato= new SimpleDateFormat("dd-MM-YYYY");
        Fecha.setText(formato.format(sisFecha));
        
        Timer tiempo = new Timer(100, new FrmMEmpleado.horas());
        tiempo.start();
    }
    
