    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        int fila = Tabla.getSelectedRow();
        if(fila>=0){
            objNEmp.MtdEliminar(Integer.parseInt(Tabla.getValueAt(fila, 0).toString()));
            modelo.removeRow(fila);
        }
        else{
            JOptionPane.showMessageDialog(null, "Seleccione una fila de la tabla");
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEstadoActionPerformed

        int fila = Tabla.getSelectedRow();
        
        if(fila>=0){
            
            if(Tabla.getValueAt(fila, 4).toString().equalsIgnoreCase("I")){
                //estado = "1";
                Tabla.getValueAt(fila, 4).toString();
                objNEmp.MtdEstado("A",Integer.parseInt(Tabla.getValueAt(fila, 0).toString()));
            }
            
            if(Tabla.getValueAt(fila, 4).toString().equalsIgnoreCase("A")){
                //estado = "0";
                Tabla.getValueAt(fila, 4).toString();        
                objNEmp.MtdEstado("I",Integer.parseInt(Tabla.getValueAt(fila, 0).toString()));
            }  
            modelo.removeRow(fila);
            MtdlimpiarTabla();   
             
        }
        else{
            JOptionPane.showMessageDialog(null, "Seleccione una fila de la tabla");
        }
        
        
    }//GEN-LAST:event_btnEstadoActionPerformed

