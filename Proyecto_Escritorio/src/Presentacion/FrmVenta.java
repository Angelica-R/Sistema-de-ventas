    public void MtdImprimir(){
        Boleta bolet = new Boleta();
        impresion imp = new impresion();
        imp.show();

        Boleta.labelDni.setText(txtDniCliente.getText());
        Boleta.labelNro.setText(labelIdVenta.getText());
        Boleta.labelFecha.setText(labelFecha.getText());
        Boleta.txtTotal.setText(txtTotal.getText());
        Boleta.Nombre.setText(txtNombreCliente.getText()+" "+txtApellidosCliente.getText());
        Boleta.txtTotalVenta.setText(txtTotalVenta.getText());
        Boleta.txtIGV.setText(txtIGV.getText());
        
        
        for(int i=0;i<modelo.getRowCount();i++){
            
            String Datos[] = new String[3];
            Datos[0]=TablaProductos.getValueAt(i, 0).toString();
            Datos[1]=TablaProductos.getValueAt(i, 2).toString();
            Datos[2]=TablaProductos.getValueAt(i, 4).toString();            
            bolet.modelo1.addRow(Datos); 
   
        }
    }
        
    private void MtdlimpiarTabla(){
        modelo.getDataVector().removeAllElements();
        TablaProductos.updateUI();
    }
    
    private void MtdId_venta() {
        labelIdVenta.setText("1");
        ResultSet rs;
        ClsNVenta objId=new ClsNVenta();
        rs=objId.MtdIdVenta();
        int incremento;
        try {
            while (rs.next())
            {
                incremento = rs.getInt("id")+1;
                labelIdVenta.setText(String.valueOf(incremento));               
            }
        } catch (SQLException ex) {
                System.out.println(ex);
        }
    }
    
