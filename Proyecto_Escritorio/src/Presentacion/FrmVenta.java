    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        ResultSet rs;
        ClsECliente objEc=new ClsECliente();
        ClsNCliente objNc=new ClsNCliente();

            int flag = 0;
        
            try {
                objEc.setDni(Integer.parseInt(txtDniCliente.getText()));
                flag = 1;
            }
            catch(Exception e) {
                JOptionPane.showMessageDialog(null,"Ingrese el Nro de DNI correctamente");
            }

            if (flag==1){
                if (objNc.MtdValidarCliente(objEc)==true)
                {
                    txtDniCliente.setEditable(false);
                    btnNuevoCliente.setEnabled(false);
                    txtNombreCliente.setEnabled(true);
                    txtApellidosCliente.setEnabled(true);
                    rs=objNc.MtdBucarCliente(objEc);

                    try {
                        while (rs.next())
                        {
                            txtNombreCliente.setText(rs.getString("nombre"));
                            txtApellidosCliente.setText(rs.getString("apellidos"));
                        }
                    } catch (SQLException ex) {
                        System.out.println(ex);
                    }
                } 
                else
                {
                     JOptionPane.showMessageDialog(null,"No hay registro del cliente");
                }  
            }
    }//GEN-LAST:event_btnBuscarActionPerformed

