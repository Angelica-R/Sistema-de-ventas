    private void btnRegistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistarActionPerformed
        
        if(modelo.getRowCount()>0){
            MtdImprimir();
        
            String fecha="";
            objECliente.setDni(Integer.parseInt(txtDniCliente.getText().toUpperCase()));        
            objECliente.setNombres(txtNombreCliente.getText().toUpperCase());
            objECliente.setApellidos(txtApellidosCliente.getText().toUpperCase());

            objEStock.setMarca(LabelMarca.getText().toUpperCase());
            JOptionPane.showMessageDialog(null,"dato de cliente guardado");
            objEStock.setStock(Integer.parseInt(LabelCantidad.getText().toUpperCase()));
            fecha=labelFecha.getText().toUpperCase()+" "+ labelHora.getText().toUpperCase();


            objEVenta.setTotal(Double.parseDouble(txtTotalVenta.getText().toUpperCase()));
            objEVenta.setFecha(fecha);
            objEVenta.setCliente_dni(Integer.parseInt(txtDniCliente.getText().toUpperCase()));
            objEVenta.setEmpleado_dni(Integer.parseInt(labelDniEmpleado.getText().toUpperCase()));

            if (objNventa.MtdRegistrarCliente(objECliente)==true)
            {
                //JOptionPane.showMessageDialog(null,"dato de cliente guardado");
            }
            else{
                JOptionPane.showMessageDialog(null,"ocurrio un error en guardar cliente");
            }

            if (objNventa.MtdRegistrarVenta(objEVenta)==true)
            {
                //JOptionPane.showMessageDialog(null,"dato de venta guardado");
            }
            else{
                JOptionPane.showMessageDialog(null,"ocurrio un error en guardar venta");
            }

            for(int i=0;i<modelo.getRowCount();i++){
                objEDVenta.setCantidad(Integer.parseInt(TablaProductos.getValueAt(i, 0).toString()));
                objEDVenta.setTotal_prod(Double.parseDouble(TablaProductos.getValueAt(i, 4).toString()));
                objEDVenta.setVenta_id(Integer.parseInt(labelIdVenta.getText().toUpperCase()));
                objEDVenta.setProducto_id(Integer.parseInt(TablaProductos.getValueAt(i, 1).toString()));
                objNventa.MtdRegistrarDetalleVenta(objEDVenta);   
            }

            for(int i=0;i<modelo.getRowCount();i++){

                objEProducto.setStock(Integer.parseInt(TablaProductos.getValueAt(i, 5).toString()));
                objEProducto.setId(Integer.parseInt(TablaProductos.getValueAt(i, 1).toString()));
                objNventa.MtdActualizarStock(objEProducto);          
            } 

            MtdReset();
        }else{
            JOptionPane.showMessageDialog(null,"no agrego ningun producto para la venta");
        }
        
        
        
    }//GEN-LAST:event_btnRegistarActionPerformed

    
