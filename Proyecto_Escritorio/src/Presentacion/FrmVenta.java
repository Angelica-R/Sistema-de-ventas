    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
      
        if(Integer.parseInt(txtcantidad.getText()) <= Integer.parseInt(LabelCantidad.getText())){
            double m;
            int a;
            double igv=0;
            double total_venta=0;
            m = Double.parseDouble(txtcantidad.getText())* Double.parseDouble(txtProdPrecio.getText());
            a = Integer.parseInt(LabelCantidad.getText())- Integer.parseInt(txtcantidad.getText());
            double suma = 0;
            String[]info = new String[6];
            info[0] = txtcantidad.getText();
            info[1] = txtProdCodigo.getText();
            info[2] = txtProdNombre.getText();
            info[3] = txtMarca.getText();
            info[4] = String.valueOf(m);
            info[5] = String.valueOf(a);
            modelo.addRow(info);
            for(int i=0;i<modelo.getRowCount();i++){
                suma = suma + Double.parseDouble(TablaProductos.getValueAt(i, 4).toString());
                
                txtTotal.setText(String.valueOf(Math.round(suma*100.0)/100.0));
                txtIGV.setText(String.valueOf(Math.round((suma*0.18)*100.0)/100.0 ));
                txtTotalVenta.setText(String.valueOf(Math.round((suma*1.18)*100.0)/100.0 ));
            }      
        }
        else{
            JOptionPane.showMessageDialog(null, "esta excediendo del stock disponible");
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

