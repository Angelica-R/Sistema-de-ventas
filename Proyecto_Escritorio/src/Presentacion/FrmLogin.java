    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        ResultSet rs;
        ClsEEmpleado objEmp=new ClsEEmpleado();
        ClsControlLogin objNemp=new ClsControlLogin();
        
        int flag1 = 0;
        int flag = 0;
        
        int dni,cla;
        dni = txtDni.getText().length();
        cla = txtClave.getText().length();
        
        if(dni == 0 || cla == 0){
            flag=0;
            JOptionPane.showMessageDialog(null,"Debe llenar todos los campos");
        }else{
            flag=1;
        }  
        if(flag==1){
            String estado = "";
        //int dni=0;
        objEmp.setDni(txtDni.getText());
        objEmp.setClave(txtClave.getText());
        String Cargo="";
        
        if (objNemp.MtdLogueoUsuario(objEmp)==true)
        {
            rs=objNemp.MtdEstado(objEmp);
                try {
                    while (rs.next())
                        {
                            estado = rs.getString("estado");
                            
                        }
                    } catch (SQLException ex) {
                        System.out.println(ex);
                    }      
                if(estado.equalsIgnoreCase("A")){
                    
                    JOptionPane.showMessageDialog(null,"inicio ");
                    
                    rs=objNemp.MtdCargo(objEmp);
                        try {
                            while (rs.next())
                                {
                                    Cargo = rs.getString("Cargo_id");
                                }
                            } catch (SQLException ex) {
                                System.out.println(ex);
                            } 
                            if(Cargo.equalsIgnoreCase("encargado de ventas")){
                                JOptionPane.showMessageDialog(null,"inicia Sistema");
                                DniEmpleado = txtDni.getText(); 
                                NombreEmpleado = txtClave.getText();
                                FrmVenta frm =new FrmVenta();
                                FrmVenta.labelDniEmpleado.setText(txtDni.getText());
                                frm.show();
                                this.dispose();
                            }
                            else if(Cargo.equalsIgnoreCase("1")){
                                JOptionPane.showMessageDialog(null,"inicia Sistema");
                                DniEmpleado = txtDni.getText(); 
                                NombreEmpleado = txtClave.getText();
                                FrmProductos FProd = new FrmProductos();
                                FProd.labelEmpleadoDni.setText(txtDni.getText());
                                FProd.show();
                                this.dispose();

                            }
                            else if(Cargo.equalsIgnoreCase("gerente general")){
                                JOptionPane.showMessageDialog(null,"inicia Sistema");
                                DniEmpleado = txtDni.getText(); 
                                NombreEmpleado = txtClave.getText();
                                FrmMEmpleado FEmp = new FrmMEmpleado();
                                FEmp.labelDni.setText(txtDni.getText());
                                FEmp.show();
                                this.dispose();
                            }
            }  
            else{
                JOptionPane.showMessageDialog(null,"Cuenta suspendida ");
                }
                
        } 
        else
        {
             JOptionPane.showMessageDialog(null,"Cuenta incorrecta");
        }    
        }
        
    }//GEN-LAST:event_btnIngresarActionPerformed
    
